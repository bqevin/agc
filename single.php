					<h2 style="text-transform:capitalize; color:#6CA12A!important; text-align:left; font-size: 1.7em;">PIM and Agri-Gender host write-shop to strengthen gender research</h2>
					<p>
						For scholars researching the intersection of gender, agriculture, and food security, few outlets exist to publish their findings. Yet such research is considered critical for developing program and policy recommendations that can achieve gender-equitable development outcomes. Without sufficient empirical evidence, interventions may fail to increase gender equality or worse yet, exacerbate existing inequalities.
						<p>The Journal of Gender, Agriculture and Food Security (Agri-Gender), an international, peer-reviewed journal, provides a forum for research on these issues. In order to build the capacity of agricultural researchers to conduct rigorous gender analyses and translate their research findings into recommendations, Agri-Gender and the CGIAR Research Program on Policies, Institutions, and Markets (PIM) co-hosted a write-shop on July 14, 2015 in Berlin, Germany. 
						For scholars researching the intersection of gender, agriculture, and food security, few outlets exist to publish their findings.
						Bringing Researchers Together
						<p>The write-shop, a one day academic paper review intensive, provided in-depth discussions on research methodologies and best practices for preparing papers for journal publication. Participants received constructive feedback from other write-shop participants and from Dr. Cheryl Doss, Senior Lecturer at Yale University and Gender Coordinator of PIM and Dr. Jemimah Njuki, Editor-in-Chief of Agri-Gender and Senior Program Officer at the International Development Research Centre (IDRC).
						<p>The ten participants, eight women and two men, came from several CGIAR Centers as well as research institutions, foundations, and intergovernmental organizations – some with years of research experience and numerous publications and others at the beginning of their careers. Their research topics ranged from analyses of the role of gender and changing social norms in specific value chains to a broader conceptual framework integrating value chain, livelihoods, and gender perspectives; from wage equity in the agricultural sector in Morocco to the influence of women’s involvement in groups on group performance across five countries in Africa and Asia; and from analysis of how social programs empower women in El Salvador to the role of social institutions in agricultural research in Latin America.
						Each write-shop participant presented a paper to the group. After these presentations, participants reviewed the contribution of new knowledge to the body of research on gender and agriculture, provided feedback on the methodologies for data collection, examined the validity of the conclusions, and identified common themes across the papers. In the afternoon, the participants broke into two groups for detailed discussions of each paper, led by Cheryl Doss and Jemimah Njuki.
						<p>Dr. Doss highlighted the need to draw out the overarching story from the research to identify the key takeaway messages and what the paper contributes to the body of knowledge on the role of gender in agriculture. She provided suggestions on how to frame and organize a paper around answering a specific research question. She noted the literature review should present the findings from previous research and identify gaps in the literature, and the text accompanying tables and figures should emphasize the patterns that help answer the research question. Finally, she provided concrete recommendations on what should and should not be included in descriptions of the sampling methods, study sites, and techniques for analysis.
						The write-shop proved instrumental in strengthening capacity for analysis and scientific writing, and sharing results and lessons from different researchers working in this area.”
						<p>The Write-shop Model Works
						<p>“My paper greatly benefitted from the write-shop in Berlin,” remarked Dina Najjar, Associate Social and Gender Scientist at ICARDA. “In particular, I appreciated the thorough comments from four readers, which were constructive and insightful. This was largely due to the format of the workshop, which required us to read each other’s papers before meeting. My questions were addressed in the write-shop, such as how to analyze some of my findings that were sex-disaggregated and how to present findings with low number of respondents. Altogether, I think the write-shop was indispensable for improving my paper.”
						<p>Dr. Cheryl Doss concurred. “The write-shop provided an excellent opportunity for researchers on a variety of topics related to gender in the areas of policies, institutions, and markets, to come together to share ideas and research.  We expect the revised papers will be substantially stronger as a result of this opportunity.  In addition, the researchers had the opportunity to see how their individual work fits into a broader set of literature.”
